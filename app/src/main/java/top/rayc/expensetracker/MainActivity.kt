class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        enableEdgeToEdge()
        setContent {
            val appState = rememberAppState()
            CompositionLocalProvider() {
                ETheme(
                    disableDynamicTheming = true
                ) {
                    EApp(appState)
                }
            }
        }
    }
}

